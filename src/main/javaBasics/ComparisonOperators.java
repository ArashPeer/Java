public class ComparisonOperators {
//What it allows us to do is to compare values
    public static void main(String[] args) {
        int aliAge = 24;
        int MujAge = 39;

        System.out.println(aliAge == MujAge);
        System.out.println(aliAge <= MujAge);
        System.out.println(aliAge != MujAge);
        System.out.println(aliAge >= MujAge);
    }
}
